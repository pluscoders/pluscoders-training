/**
 * Prints a salutation of a given person in the console.
 *
 * @example
 *
 * hello('Evgy')
 * > Hello, Evgy!
 *
 * @example
 *
 * hello('Manu')
 * > Hello, Manu!
 *
 * @param {string} name The name of the person you want to salute.
 */
function hello(name) {
  console.log("Hello, " + name + "!");
}
