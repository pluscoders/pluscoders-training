/**
 * Converts an amount of euros into roubles.
 *
 * @example
 *
 * eurosToRoubles(100)
 * > 8500
 *
 * @example
 *
 * eurosToRoubles(4)
 * > 340
 *
 * @param {number} euros The euros to convert into robules.
 *
 * @returns {number} The resulting roubles from euros.
 */
function eurosToRoubles(euros) {
  return euros * 85;
}
