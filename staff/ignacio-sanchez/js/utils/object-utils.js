/**
 * Checks a character is undefined.
 * 
 * @param {string} obj The character to evaluate.
 * 
 * @returns {boolean} 
 */
function isUndefined(obj) {
    return obj === undefined
}
/**
 * Checks a character is not undefined.
 * 
 * @param {string} obj The character to evaluate.
 * 
 * @returns {boolean} 
 */
function isNotUndefined(obj) {
    return obj !== undefined
}